val promptFinalizer = """
    # INSTRUCTIONS:
    You receive a single message generated by another LLM agent.
    
    Your goal is to analyze the message and produce a JSON output with two fields:
      1) followUp detection
      2) language span segmentation
    
    ## TASK 1:
    Determine whether the message explicitly asks the user for additional input.
    Set "followUp" to true ONLY if the message clearly asks the user a question or requests a reply.
    
    Examples:
      "What else can I do for you?" -> true
      "Do you want me to book the taxi?" -> true
      "Sure, calling Ricky for you." -> false
      "Your booking is confirmed." -> false
    
    Rules:
      - If the message only informs the user or performs an action, return false.
      - If you are uncertain, return false.
      - Only explicit questions or requests for user input count as follow-up.
    
    Set as false if you are in doubt.
    
    ## TASK 2:
    The message may contain text written in multiple languages.
    Your task is to split the message into contiguous spans whenever the language changes.
    
    Rules:
      - Each span must contain text written in a single language.
      - Only split when the language changes.
      - Do NOT split inside the same language.
      - Preserve the original order of the text.
      - The spans combined must reproduce the original message exactly.
      - Do NOT omit words, punctuation, or spaces.
      - Do NOT create empty spans or spans with only punctuation.
    
    **Reason carefully before splitting: every name that is present in a different language must be correctly identified!**.
    
    Output format for each span:
      - "text": the exact substring from the message
      - "langCode": a 2-letter language code
    
    Allowed language codes: $messLangCodes.
    If the language of a span cannot be confidently identified, use "${prefs.queryLanguage}".
    
    Example:
      input: "i want to go to stazione termini a roma"
      output:
        - text: "i want to go to "
          langCode: "en"
        - text: "stazione termini a roma"
          langCode: "it"
    
    # OVERALL OUTPUT:
    Return ONLY a valid JSON object with the following structure:

    {
      "followUp": boolean,
      "spans": [
        {
          "text": "string",
          "langCode": "string"
        }
      ]
    }
    
    Requirements:
      - The response must be valid JSON.
      - Do not include explanations.
      - Do not include markdown.
      - Do not include backticks.
""".trimIndent()
